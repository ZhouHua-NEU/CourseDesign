package datastruct;

//生成一个实体类community，包含私有变量communityid，communityname，communityaddress，communityphone，communitymanager
public class Community {
    private String communityname;
    private String communityaddress;
    private String communityphone;
    private String communitymanager;
    

    //get方法
    public String getCommunityname() {
        return communityname;
    }

    //set方法
    public void setCommunityname(String communityname) {
        this.communityname = communityname;
    }

    //get方法

    public String getCommunityaddress() {
        return communityaddress;
    }

    //set方法
    public void setCommunityaddress(String communityaddress) {
        this.communityaddress = communityaddress;
    }

    //get方法
    public String getCommunityphone() {
        return communityphone;
    }

    //set方法
    public void setCommunityphone(String communityphone) {
        this.communityphone = communityphone;
    }

    //get方法
    public String getCommunitymanager() {
        return communitymanager;
    }

    //set方法
    public void setCommunitymanager(String communitymanager) {
        this.communitymanager = communitymanager;
    }


}
